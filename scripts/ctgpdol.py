# Parse stage3 to reconstruct the DOL header
def parse_new_stage3(stage3, stage4out, base):
    base -= 0x3F10

    text = (struct.unpack(">I", stage3[base+0x3F10:base+0x3F14])[0] << 16) & 0xFFFF0000
    text |= struct.unpack(">I", stage3[base+0x3F1C:base+0x3F20])[0] & 0xFFFF
    if text & 0x8000:
        text -= 0x10000

    text_addr = (struct.unpack(">I", stage3[base+0x3F18:base+0x3F1C])[0] << 16) & 0xFFFF0000
    text_addr |= struct.unpack(">I", stage3[base+0x3F20:base+0x3F24])[0] & 0xFFFF
    if text_addr & 0x8000:
        text_addr -= 0x10000

    text_size = (struct.unpack(">I", stage3[base+0x3F14:base+0x3F18])[0] << 16) & 0xFFFF0000
    text_size |= struct.unpack(">I", stage3[base+0x3F24:base+0x3F28])[0] & 0xFFFF
    if text_size & 0x8000:
        text_size -= 0x10000

    data = (struct.unpack(">I", stage3[base+0x3F48:base+0x3F4C])[0] << 16) & 0xFFFF0000
    data |= struct.unpack(">I", stage3[base+0x3F54:base+0x3F58])[0] & 0xFFFF
    if data & 0x8000:
        data -= 0x10000

    data_addr = (struct.unpack(">I", stage3[base+0x3F50:base+0x3F54])[0] << 16) & 0xFFFF0000
    data_addr |= struct.unpack(">I", stage3[base+0x3F58:base+0x3F5C])[0] & 0xFFFF
    if data_addr & 0x8000:
        data_addr -= 0x10000

    data_size = (struct.unpack(">I", stage3[base+0x3F4C:base+0x3F50])[0] << 16) & 0xFFFF0000
    data_size |= struct.unpack(">I", stage3[base+0x3F5C:base+0x3F60])[0] & 0xFFFF
    if data_size & 0x8000:
        data_size -= 0x10000

    bss_addr = (struct.unpack(">I", stage3[base+0x3F38:base+0x3F3C])[0] << 16) & 0xFFFF0000
    bss_addr |= struct.unpack(">I", stage3[base+0x3F70:base+0x3F74])[0] & 0xFFFF
    if bss_addr & 0x8000:
        bss_addr -= 0x10000

    bss_size = (struct.unpack(">I", stage3[base+0x3F7C:base+0x3F80])[0] << 16) & 0xFFFF0000
    bss_size |= struct.unpack(">I", stage3[base+0x3F84:base+0x3F88])[0] & 0xFFFF
    if bss_size & 0x8000:
        bss_size -= 0x10000

    stage4out.write(struct.pack(">I", text))
    stage4out.write(bytearray(0x18))
    stage4out.write(struct.pack(">I", data))
    stage4out.write(bytearray(0x28))

    stage4out.write(struct.pack(">I", text_addr))
    stage4out.write(bytearray(0x18))
    stage4out.write(struct.pack(">I", data_addr))
    stage4out.write(bytearray(0x28))

    stage4out.write(struct.pack(">I", text_size))
    stage4out.write(bytearray(0x18))
    stage4out.write(struct.pack(">I", data_size))
    stage4out.write(bytearray(0x28))

    stage4out.write(struct.pack(">I", bss_addr))
    stage4out.write(struct.pack(">I", bss_size))
    stage4out.write(struct.pack(">I", text_addr))
    stage4out.write(bytearray(0x1C))

